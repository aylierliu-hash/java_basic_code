package com.itheima.arithmaticoperater;

public class ArithmeticoperatorDemo5 {
        public static void main(String[] args) {
            //++和--
            int a =  10;
            a++;
            System.out.println(a);
            ++a;
            System.out.println(a);
            a--;
            System.out.println(a);
            --a;
            System.out.println(a);
        }
}
